from talo import Talo

def main():
    talo = Talo(1, 10, 3)

    print("Ajetaan ensimmäistä hissiä kerrokseen 5:")
    talo.aja_hissia(0, 5)

    print("\nAjetaan toista hissiä kerrokseen 3:")
    talo.aja_hissia(1, 3)

    print("\nPalautetaan ensimmäinen hissi takaisin alimpaan kerrokseen:")
    talo.aja_hissia(0, 1)

    print("\nPalohälytys:")
    talo.palohalytys()

if __name__ == "__main__":
    main() 
